/**
 * This class contains all the available equipment
 * @author Carlos
 *
 */
public class Equipment {
	/**
	 * This method prints out the information for the Helmet
	 * 
	 * @throws InterruptedException
	 */
	public static void helmet() throws InterruptedException{
		System.out.println("Helmet.");
        Thread.sleep(2000);
		System.out.println("Adds 3 Shield.");
        Thread.sleep(2000);
	}
	/**
	 * This method prints out the information for the Chest Plate
	 * 
	 * @throws InterruptedException
	 */
	public static void chestPlate() throws InterruptedException{
		System.out.println("Chest Plate.");
        Thread.sleep(2000);
		System.out.println("Adds 5 Shield.");
        Thread.sleep(2000);
	}
	/**
	 * This method prints out the information for the Leggings
	 * 
	 * @throws InterruptedException
	 */
	public static void leggings() throws InterruptedException{
		System.out.println("Leggings.");
        Thread.sleep(2000);
		System.out.println("Adds 4 shield.");
        Thread.sleep(2000);
	}
}
